# > Mayor que, true o false
print(7 > 3) # True
print(3 > 7) # False
print(7 > 7) # False

# < Menor que
print(5 < 6) # True
print(6 < 5) # False
print(5 < 5) # False

# >= Mayor o igual
print(2 >= 1) # True
print(2 >= 3) # False
print(2 >= 2) # True

# <= Menor o igual
print(1 <= 2) # True
print(2 <= 1) # False
print(2 <= 2) # True


# == Estrictamente igual
print(6 == 6) # True
print(5 == 2) # False

# != Diferente a
print(6 != 10) # True
print(6 != 6) # False

print("Apple" == 'Apple') # True, sin diferencia por comillas
print("Apple" == 'pple') # False, diferencia por mayusculas
print("1" == 1) # False, diferencia en tipos de datos

age = 18
print("Es la edad mator a 18?", age >= 18) # Validar una edad